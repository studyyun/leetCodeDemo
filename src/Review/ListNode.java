package Review;

/**
 * 应用模块名称
 *
 * @author zhousy
 * @date 2022-02-17  14:40
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
